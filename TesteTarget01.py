INDICE = 13  # Definimos o valor do índice, ou seja, o número limite da sequência
SOMA = 0     # Iniciamos a variável SOMA com o valor zero
K = 0        # Inicializamos a variável K com zero

while K < INDICE:  # Criamos um loop que continua até K ser igual ao valor de INDICE
    K += 1         # Incrementamos o valor de K em 1 a cada iteração
    SOMA += K      # Somamos o valor de K à variável SOMA

print(SOMA)        # Exibimos o valor final de SOMA, que será 91
